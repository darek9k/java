package javaCourse;

public class Circle {
    public static void main(String[] args) {
        int r = 10;

        double v = 2 * 3.1415 * r;
        System.out.println("Circumference of a circle: " + v);

        double v1 = 3.1415 * r * r;
        System.out.println("Circle area: " + v1);

    }
}
